async def main():
    db = Database(Path("/opt/project-forge/data/forge.db"))
    await db.connect()

    existing = await db.count_ideas()
    print(f"Existing ideas in DB: {existing}")

    seeded = 0
    for idea in IDEAS:
        await db.save_idea(idea)
        seeded += 1
        if seeded % 10 == 0:
            print(f"  Seeded {seeded}/{len(IDEAS)}...")

    await db.close()
    total = existing + seeded
    print(f"\nSeeded {seeded} new ideas (total: {total})")
    print(f"Visit http://localhost:55443 to view the dashboard")
    print(f"\nBreakdown by category:")
    cats = {}
    for idea in IDEAS:
        cats[idea.category.value] = cats.get(idea.category.value, 0) + 1
    for cat, count in sorted(cats.items()):
        print(f"  {cat}: {count}")


if __name__ == "__main__":
    asyncio.run(main())
